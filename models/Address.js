import mongoose from "mongoose";
const Schema = mongoose.Schema;

const addressSchema = new Schema({
  street: {
    type: String,
    default: "",
  },
  apartment: {
    type: String,
    default: "",
  },
  zip: {
    type: String,
    default: "",
  },
  city: {
    type: String,
    default: "",
  },
  country: {
    type: String,
    default: "",
  },
});

export default mongoose.model("Address", addressSchema);
